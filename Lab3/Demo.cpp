#include <iostream>
#include <cmath>

using namespace std;

int main(){



    cout << floor(2.6) << endl; //2
    cout << floor(2.1) << endl; //2

    cout << ceil(2.6) << endl; //3
    cout << ceil(2.1) << endl; //3

    cout << round(2.6) << endl; //3
    cout << round(2.1) << endl; //2



    return 0;

}